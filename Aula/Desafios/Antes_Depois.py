Numero_Int = int(input('Digite um Número Inteiro: '))
Numero_Float = float(input('Digite um Número com Float: '))

print(f'\nValor Digitado: {Numero_Int} \nNúmero Antes: {Numero_Int - 1 : .2f} \nNúmero Depois: {Numero_Int + 1 : .2f}')
print(f'\nValor Digitado: {Numero_Float : .2f} \nNúmero Antes: {Numero_Float - 1 : .2f} \nNúmero Depois: {Numero_Float + 1 : .2f}')